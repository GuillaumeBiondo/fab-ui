  <Button
    data-sidebar="trigger"
    data-slot="sidebar-trigger"
    shape="ghost"
    :icon="true"
    :class="cn('h-7 w-7', props.class)"
    @click="toggleSidebar"
  >
    <PanelLeftOpen v-if="isMobile || state === 'collapsed'" />
    <PanelLeftClose v-else />
    <span class="sr-only">Toggle sidebar</span>
  </Button>
